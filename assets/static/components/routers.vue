<script>
module.exports = {
  name: 'routers',
  data: function(){
    return{
    dataTable: null,
    m_form_search: "",
    show_loading: false,
    show_add_errors: false,
    show_edit_errors: false,
    show_add_messages: "",
    headings: [
      {column: "Actions", id: "actions"},
      {column: "Name", id: "name"},
      {column: "IP", id: "ip"},
      {column: "HTTP Port", id: "http_port"},
      {column: "Username", id: "username"},
      {column: "Password", id: "password"},
      {column: "Monitoring", id: "monitoring"},
      {column: "Created At", id: "created_at"},
    ],
    form_labels: {
      name: "Name",
      ip: "IP",
      username: "Username",
      password: "Password",
      http_port: "HTTP Port",
      status: "Monitoring",
      add_title: "Add Router",
      edit_title: "Edit Router",
      hide_show_title: "Show/Hide Columns",
      add_router_button: "Add Router",
      hide_show_button: "OK",
      submit_button: "Save changes"
    },
    router_name: "",
    router_ip: "",
    router_username: "",
    router_password: "",
    http_router_port: "",
    router_is_monitoring: false,
    edit_router_id: "",
    edit_router_name: "",
    edit_router_ip: "",
    edit_router_username: "",
    edit_router_password: "",
    edit_http_router_port: "",
    edit_router_is_monitoring:  false,
    user_id: ""
    }
  },
  methods: {
    initializeTable: function(){
      let routersDataTable = $('#routers-datatable').DataTable({
        fnInitComplete: function(){
          // Enable TFOOT scoll bars
          $('.dataTables_scrollFoot').css('overflow', 'auto');
          $('.dataTables_scrollHead').css('overflow', 'auto');
          // Sync TFOOT scrolling with TBODY
          $('.dataTables_scrollFoot').on('scroll', function () {
            $('.dataTables_scrollBody').scrollLeft($(this).scrollLeft());
          });
          $('.dataTables_scrollHead').on('scroll', function () {
            $('.dataTables_scrollBody').scrollLeft($(this).scrollLeft());
          });
        },
        ajax: {
        url: "/routers/data",
          dataSrc: function(data) {
            return data.routers;
          },
          error: function(xhr, error, thrown) {
            if (xhr.responseJSON) {
              console.log(xhr.responseJSON.message);
            } else {
              console.log("Something went wrong, Please try again.");
            }
          }
        },
        columns: [
        {
          class: "text-center actions",
          data: function(row, type, set, meta) {
            return '<div id="action_btn"><div class="editRouter cursor_to_pointer fa fa-edit" data-id="'+ row.id +'"></div> <div class="cursor_to_pointer fa fa-trash delRouter" data-id="'+ row.id +'"></div></div>';
          }
        },
        {
          class: "name",
          data: function(row, type, set, meta) {
            return row.name;
          }
        },
        {
          class: "ip",
          data: function(row, type, set, meta) {
            return row.ip;
          }
        },
        {
          class: "text-center http_port",
          data: function(row, type, set, meta) {
            return row.port;
          }
        },
        {
          class: "text-center username",
          data: function(row, type, set, meta) {
            return row.username;
          }
        },
        {
          class: "text-center password",
          data: function(row, type, set, meta) {
            return row.password;
          }
        },
        {
          class: "text-center monitoring",
          data: function(row, type, set, meta) {
            return row.is_monitoring;
          }
        },
        {
          class: "text-center created_at",
          data: function(row, type, set, meta) {
            return moment(row.created_at).format('DD/MM/YYYY HH:mm:ss');
          },
        },
        ],
        autoWidth: true,
        info: false,
        bPaginate: false,
        lengthChange: false,
        scrollX: true,
        colReorder: true,
        stateSave:  true
      });
      return this.dataTable = routersDataTable;
      this.dataTable.search("");
    },
    search: function(){
      this.dataTable.search(this.m_form_search).draw();
    },
    showHideColumns: function(id){
      let column = this.dataTable.columns("." +id);
      if(column.visible()[0] == true){
        column.visible(false);
      }else{
        column.visible(true);
      }
    },
    onROUTERButton: function() {
      $(this.$refs.addmodal).modal("show");
    },
    onROUTERHideShowButton: function() {
      $(this.$refs.hideShow).modal("show");
    },
    saveModal: function() {
      this.show_loading = true;
      this.show_add_errors = true;
      this.$http.post('/routers', {
        name: this.router_name,
        ip:  this.router_ip,
        username: this.router_username,
        password: this.router_password,
        port: this.http_router_port,
        is_monitoring: this.router_is_monitoring,
        user_id: this.user_id
      }).then(function (response) {
        $.notify({message: 'Router has been added.'},{type: 'info'});
        this.show_loading = false;
        this.dataTable.ajax.reload();
        this.clearForm();
        $(this.$refs.addmodal).modal("hide");
      }).catch(function (error) {
        this.show_add_messages = error.body.errors;
        this.show_add_errors = true;
        this.show_loading = false;
      });
   },
    clearForm: function() {
      this.router_name = "";
      this.router_ip = "";
      this.router_username = "";
      this.router_password = "";
      this.http_router_port = "";
      this.router_is_monitoring = false;
      this.show_add_errors = false;
    },
    getUniqueIdentifier: function(routersDataTable){
      $(document).on("click", ".editRouter", function(){
        let tr = $(this).closest('tr');
        let row = routersDataTable.row(tr);
        let data = row.data();
        let router_id = $(this).data("id");
        module.exports.methods.onRouterEditButton(router_id, data);
      });
    },
    onRouterEditButton: function(router_id, data) {
        $("#edit_router_id").val(router_id);
        $("#edit_router_name").val(data.name);
        $("#edit_router_ip").val(data.ip);
        $("#edit_router_username").val(data.username);
        $("#edit_router_password").val(data.password);
        $("#edit_http_router_port").val(data.port);
        if (data.is_monitoring == true) {
          $("#edit_router_is_monitoring").prop( "checked", true );
        }
        $('#edit_router_to_db').modal('show');
    },
    updateRouterdo: function(){
      this.show_loading = true;
      this.show_edit_errors = true;
      let routerID         = $("#edit_router_id").val();
      let name             = $("#edit_router_name").val(),
          IP               = $("#edit_router_ip").val(),
          username         = $("#edit_router_username").val(),
          password         = $("#edit_router_password").val(),
          http_router_port = $("#edit_http_router_port").val(),
          is_monitoring    = $("#edit_router_is_monitoring").val();

      this.$http.patch("/routers/" + routerID, {
        name: $("#edit_router_name").val(),
        ip: $("#edit_router_ip").val(),
        username: $("#edit_router_username").val(),
        password: $("#edit_router_password").val(),
        port: $("#edit_http_router_port").val(),
        is_monitoring: $('#edit_router_is_monitoring').is(':checked'),
        id: routerID
      }).then(function (response) {
        $.notify({message: 'Router has been updated.'},{type: 'info'});
        this.show_loading = false;
        this.dataTable.ajax.reload();
        this.editClearFrom();
        $(this.$refs.editmodal).modal("hide");
      }).catch(function (error) {
        this.show_loading = false;
        this.show_edit_messages = error.body.errors;
        this.show_edit_errors = true;
      });
    },
    editClearFrom: function() {
      this.edit_router_id = "";
      this.edit_router_name = "";
      this.edit_router_ip = "";
      this.edit_router_username = "";
      this.edit_router_password = "";
      this.edit_http_router_port = "";
      this.edit_router_is_monitoring = false;
      this.show_loading = false;
      this.show_edit_errors = false;
    },
    initHideShow: function(){
      $(".nvr-column").each(function(){
        let routersDataTable = $('#routers-datatable').DataTable();
        let that = $(this).attr("data-id");
        let column = routersDataTable.columns("." +that);
        if(column.visible()[0] == true){
          $(this).prop('checked', true);
        }else{
          $(this).prop('checked', false);
        }
      });
    },
    get_session: function(){
      this.$http.get('/get_porfile').then(response => {
        this.user_id = response.body.id;
      });
    },
    deleteRouter: function(){
    $(document).on("click", ".delRouter", function(){
      let routerRow, result;
      routerRow = $(this).closest('tr');
      let routerID = $(this).data("id");
      result = confirm("Are you sure to delete this Router?");
      if (result === false) {
        return;
      }
      let data = {};
      data.id = routerID;
      let settings;
      settings = {
        cache: false,
        data: data,
        dataType: 'json',
        error: function(){return false},
        success: function(){
          routerRow.remove();
          $.notify({message: 'Router has been deleted.'},{type: 'info'});
          return true;
        },
        contentType: "application/x-www-form-urlencoded",
        context: {routerRow: routerRow},
        type: "DELETE",
        url: "/routers/" + routerID
      };
      $.ajax(settings);
    });
   }
  },
  mounted(){
    let table = this.initializeTable();
    this.getUniqueIdentifier(table);
    this.get_session();
    this.dataTable.search("");
    this.initHideShow();
    this.deleteRouter();
  }
}
</script>

